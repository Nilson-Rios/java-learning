package wrap.pacote;

public class OperadoresRelacionais {
	public static void main(String[] args) {
		int var1 = 2000;
		int var2 = 1984;
		
		System.out.println((var1 < var2));
		System.out.println((var1 > var2));
		System.out.println((var1 <= var2));
		System.out.println((var1 >= var2));
		System.out.println((var1 == var2));
		System.out.println((var1 != var2));
	}
}
