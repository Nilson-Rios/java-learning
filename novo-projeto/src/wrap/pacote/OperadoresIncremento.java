package wrap.pacote;

public class OperadoresIncremento {
	public static void main(String[] args) {
	int var1 = 1;
	
		System.out.println(++var1);
		var1 = 1;
		System.out.println(--var1);
		var1 = 1;
		System.out.println(var1++);
		System.out.println("- " + var1);
		var1 = 1;
		System.out.println(var1--);
		System.out.println(var1);
		var1 = 1;
	}
}
